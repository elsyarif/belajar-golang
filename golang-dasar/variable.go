package main

import "fmt"

func main() {
	var nama string
	nama = "Syarif"
	fmt.Println(nama)

	nama = "Syarif Hidayatulloh"
	fmt.Println(nama)

	// variabel tidak menggunakan kata kunci menggunakan :=
	namalengkap := "Syarif Hidayatulloh"
	fmt.Println(namalengkap)
	//merubah variable nama langung
	namalengkap = "Hidayatulloh"
	fmt.Println(namalengkap)

	// Deklarasi multiple variable
}
